// ES6 предоставяляет более простой способ вставки значения переменной или
// результата выражения (т.н. "интерполяцию"), которые рассчитываются
// автоматически.


// ${ ... } используется для вычисления значения переменной/выражения.
// `` Обратные кавычки используются как разделитель для таких случаев.

let user = 'Кевин';
console.log(`Привет, ${user}!`); // Привет, Кевин!